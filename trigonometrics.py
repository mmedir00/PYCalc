import math

class trigonometrics:
    def __init__(self):
        pass
        
    

    def cos(self, angle):
        return math.cos(float(angle))
    
    def sin(self, angle):
        return math.sin(int(angle))
    
    def tan(self, angle):
        return math.tan(angle)
    
    def acs(self, angle):
        return math.acos(angle)
    
    def asn(self, angle):
        return math.asin(angle)
    
    def atg(self, angle):
        return math.atan(angle)